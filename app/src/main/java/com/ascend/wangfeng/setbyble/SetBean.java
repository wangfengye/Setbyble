public class SetBean {
    private String hd;
    private String bd;

    public SetBean(String hd, String bd) {
        this.hd = hd;
        this.bd = bd;
    }

    public String getHd() {
        return hd;
    }

    public void setHd(String hd) {
        this.hd = hd;
    }

    public String getBd() {
        return bd;
    }

    public void setBd(String bd) {
        this.bd = bd;
    }
}
